package com.example.demo.DTOs;

public class PlaceInfo {
    private Long id;
    private String parkingname;
    private String blockname;
    private String placename;
    private String matricule;
    private Long idparking;
    private Long placeId;
    private String Reservationidentif;
    public Long getId() {
        return id;
    }
    public void setId(Long id) {
        this.id = id;
    }
    public String getParkingname() {
        return parkingname;
    }
    public void setParkingname(String parkingname) {
        this.parkingname = parkingname;
    }
    public String getBlockname() {
        return blockname;
    }
    public void setBlockname(String blockname) {
        this.blockname = blockname;
    }
    public String getPlacename() {
        return placename;
    }
    public void setPlacename(String placename) {
        this.placename = placename;
    }
    public String getMatricule() {
        return matricule;
    }
    public void setMatricule(String matricule) {
        this.matricule = matricule;
    }
    public Long getIdparking() {
        return idparking;
    }
    public void setIdparking(Long idparking) {
        this.idparking = idparking;
    }
    public Long getPlaceId() {
        return placeId;
    }
    public void setPlaceId(Long placeId) {
        this.placeId = placeId;
    }
    public String getReservationidentif() {
        return Reservationidentif;
    }
    public void setReservationidentif(String reservationidentif) {
        Reservationidentif = reservationidentif;
    }

     
         
}
